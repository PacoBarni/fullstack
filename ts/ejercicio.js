var sumar1 = function (param1, param2) {
    return param1 + param2;
};
console.log(sumar1(6, 3));
// forma simplificada de la función de flecha
var sumarSimple1 = function (param1, param2) { return param1 + param2; };
console.log(sumarSimple1(6, 3));
